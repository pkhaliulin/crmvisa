        // Глобальный каталог документов — каждый документ создаётся ОДИН РАЗ.
        // Привязка к странам — в CountryVisaRequirementsSeeder.
        //
        // type:          'upload'   → загружают скан
        //                'checkbox' → физический / онлайн документ, просто отмечают «готово»
        // is_repeatable: true → можно добавить несколько экземпляров (напр. метрики детей)
        // metadata_schema: JSON-схема параметров (min_balance_usd, period_months, min_coverage_eur)
        //
        // AI-поля (миграция 2026_03_12_300001):
        // ai_extraction_schema: что AI извлекает из документа
        // ai_validation_rules: правила валидации извлечённых данных
        // ai_stop_factors: что блокирует визу (СТОП-факторы)
        // ai_success_factors: что повышает шансы
        // ai_risk_indicators: индикаторы риска для менеджера
        // manager_instructions: детальная инструкция менеджеру
        // max_age_days: макс. возраст документа в днях
        // confidence_criteria: критерии уверенности AI (100/50/20%)

        $templates = $this->getTemplates();

        // Проверяем наличие AI-колонок
        $hasAiColumns = \Illuminate\Support\Facades\Schema::hasColumn('document_templates', 'ai_enabled');

        foreach ($templates as $tpl) {
            $data = [
                'id'              => (string) Str::uuid(),
                'slug'            => $tpl['slug'],
                'name'            => $tpl['name'],
                'category'        => $tpl['category'],
                'type'            => $tpl['type'],
                'is_repeatable'   => $tpl['is_repeatable'],
                'sort_order'      => $tpl['sort_order'],
                'description'     => $tpl['description'],
                'metadata_schema' => isset($tpl['metadata_schema']) ? json_encode($tpl['metadata_schema']) : null,
                'is_active'       => true,
                'created_at'      => now(),
                'updated_at'      => now(),
            ];

            $updateFields = ['name', 'category', 'type', 'is_repeatable', 'description', 'metadata_schema', 'sort_order', 'updated_at'];

            if ($hasAiColumns) {
                $data['ai_enabled']           = $tpl['ai_enabled'] ?? true;
                $data['ai_extraction_schema'] = isset($tpl['ai_extraction_schema']) ? json_encode($tpl['ai_extraction_schema']) : null;
                $data['ai_validation_rules']  = isset($tpl['ai_validation_rules']) ? json_encode($tpl['ai_validation_rules']) : null;
                $data['ai_stop_factors']      = isset($tpl['ai_stop_factors']) ? json_encode($tpl['ai_stop_factors']) : null;
                $data['ai_success_factors']   = isset($tpl['ai_success_factors']) ? json_encode($tpl['ai_success_factors']) : null;
                $data['ai_risk_indicators']   = isset($tpl['ai_risk_indicators']) ? json_encode($tpl['ai_risk_indicators']) : null;
                $data['manager_instructions'] = $tpl['manager_instructions'] ?? null;
                $data['translation_required'] = $tpl['translation_required'] ?? false;
                $data['max_age_days']         = $tpl['max_age_days'] ?? null;
                $data['confidence_criteria']  = isset($tpl['confidence_criteria']) ? json_encode($tpl['confidence_criteria']) : null;

                $updateFields = array_merge($updateFields, [
                    'ai_enabled', 'ai_extraction_schema', 'ai_validation_rules',
                    'ai_stop_factors', 'ai_success_factors', 'ai_risk_indicators',
                    'manager_instructions', 'translation_required', 'max_age_days', 'confidence_criteria',
                ]);
            }

            DB::table('document_templates')->upsert($data, ['slug'], $updateFields);
        }
    }

